package lesson01_introduction;

// Simple program to print Hello, World
// A program to be executable needs to have a main method
// Each program needs at least one class
// Each program needs too one method at least
public class HelloWorld {
	public static void main(String[] args) {
		System.out.println("Hello, World!");
	}
	public static void hello() {
		System.out.println("Hello, from the World!");
	}
}
